/// Material Design 3 Spacing Tokens
/// Based on 4dp grid system
class AppSpacing {
  AppSpacing._();

  /// 4dp - Extra small (icon padding, tight gaps)
  static const double xs = 4;

  /// 8dp - Small (chip padding, list item spacing)
  static const double sm = 8;

  /// 12dp - Small-medium
  static const double smd = 12;

  /// 16dp - Medium (card padding, section spacing)
  static const double md = 16;

  /// 20dp - Medium-large
  static const double mlg = 20;

  /// 24dp - Large (content margins, card gaps)
  static const double lg = 24;

  /// 32dp - Extra large (section gaps)
  static const double xl = 32;

  /// 48dp - 2x Extra large (major section breaks)
  static const double xxl = 48;

  /// 64dp - 3x Extra large
  static const double xxxl = 64;

  // ─────────────────────────────────────────────────────────────────────────────
  // SCREEN MARGINS (based on M3 layout)
  // ─────────────────────────────────────────────────────────────────────────────

  /// Compact screen margin (16dp)
  static const double screenMarginCompact = 16;

  /// Medium screen margin (24dp)
  static const double screenMarginMedium = 24;

  /// Expanded screen margin (24dp)
  static const double screenMarginExpanded = 24;
}
